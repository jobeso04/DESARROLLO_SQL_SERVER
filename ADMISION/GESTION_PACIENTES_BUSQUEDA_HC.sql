  SELECT 'EN ATENCION ARCHIVOS, TABLA ARCHIVO_MOV' AS UBICACION, PACIENTE, HISTORIA, NOMBRES  FROM [SIGSALUD].[dbo].[ARCHIVO_MOV]   WHERE NOMBRES LIKE  '%' + @lcnombre + '%' 
  UNION ALL
  SELECT 'EN ATENCION CAJA, TABLA PAGOC' AS UBICACION, PACIENTE, '' AS HISTORIA, NOMBRE AS NOMBRES  FROM [SIGSALUD].[dbo].[PAGOC]  WHERE NOMBRE LIKE  '%' + @lcnombre + '%' 
