CREATE PROCEDURE [dbo].[SP_GET_CONSULTORIO_21_OBS]
AS BEGIN

/* @MODULO: Emergencia
 *
 */

SELECT
consultorio,
SUBSTRING(nombre, 1, CHARINDEX(' ', nombre)) nombre
FROM CONSULTORIO
WHERE TIPO = 'E' and CONSULTORIO IN ('4050', '4060')

END


GO


